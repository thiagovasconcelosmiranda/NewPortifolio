let modal = document.querySelector('.container--modal');
document.querySelector('.project-open').addEventListener('click', () => {
    modal.style.display = "none";
    window.location.reload();
});

function card(id) {
    let list = listCard['card' + id];
    let img = document.querySelector(`.card${id} img`);
    dataModal(list);
    document.querySelectorAll('.project--image img').forEach(modalItem => {
        modalItem.src = img.src;
    });
    modal.style.display = "flex";
}

function dataModal(list) {
    let iconHtml = document.querySelector('.project-icons');
    let modal = document.querySelector('.modal-body .group-col');
    let title = modal.querySelector('.project--title h1');
    let description = modal.querySelector('.project--desctition p');
    let clickImg = document.querySelector('.project--images img');

    title.innerHTML = list.title;
    description.innerHTML = list.description;
    iconHtml.innerHTML = "";

    list.icon.map(i => {
        iconHtml.innerHTML += i;
    });
    if (list.url) {
        clickImg.style.cursor = 'pointer';
    }

    clickImg.addEventListener('click', () => {
        if (list.url) {
            window.open(list.url, '_blank');
        }
    });
}






